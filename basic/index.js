/*
variabel pada javascript = 
    variabel adalah wadah untuk menampung nilai yang kita buat jadi statement
    const = tidak dapat diubah, tidak dapat muncul dengan nama variabel yang sama
    let = dapat diubah, bisa muncul lagi dengan nama variabel yang sama

expression dan statement =
    expression adalah hasil dari statement (perintah) yang kita buat atau perintah yang kita masukkan pada statement
    statement adalah perintah yang akan kita buat dan jalankan

operator perbandingan =
    == membandingkan kedua nilai tanpa melihat tipe data (kalo dianggap sama benar)
    != membandingkan kedua nilai tanpa melihat tipe data (kalo sama salah kalo beda benar)
    === membandingkan kedua nilai melihat tipe data (kalo sama benar)
    !== membandingkan kedua nilai melihat tipe data (kalo sama salah, kalo beda benar)

operator logika =
    && akan menghasilkan true jika kedua nilai benar
    || akan menghasilkan true jika salah satu nilai benar, jika keduanya salah maka false
    ! akan mengubah true ke false dan false ke true
*/

